import fs from "node:fs";
import https from "node:https";
import path from "node:path";

const postsDir = path.resolve("_posts");
const outFile = path.resolve("_data/github_repos.yml");
const token = process.env.GITHUB_TOKEN;

function requestJson(apiPath) {
  const headers = {
    Accept: "application/vnd.github+json",
    "User-Agent": "cyaris.github.io repo metadata updater",
    "X-GitHub-Api-Version": "2022-11-28",
  };

  if (token) {
    headers.Authorization = `Bearer ${token}`;
  }

  return new Promise((resolve, reject) => {
    https
      .get({ hostname: "api.github.com", path: apiPath, headers }, (res) => {
        let body = "";
        res.setEncoding("utf8");
        res.on("data", (chunk) => {
          body += chunk;
        });
        res.on("end", () => {
          let payload;
          try {
            payload = JSON.parse(body);
          } catch (error) {
            reject(new Error(`GitHub returned invalid JSON for ${apiPath}: ${error.message}`));
            return;
          }

          if (res.statusCode < 200 || res.statusCode >= 300) {
            const message = payload.message ? `: ${payload.message}` : "";
            reject(new Error(`GitHub request failed for ${apiPath} (${res.statusCode})${message}`));
            return;
          }

          resolve(payload);
        });
      })
      .on("error", reject);
  });
}

function parseFrontMatter(file) {
  const contents = fs.readFileSync(file, "utf8");
  const match = contents.match(/^---\n([\s\S]*?)\n---/);
  if (!match) {
    return {};
  }

  return Object.fromEntries(
    match[1]
      .split("\n")
      .map((line) => line.match(/^([A-Za-z0-9_-]+):\s*(.*?)\s*$/))
      .filter(Boolean)
      .map((match) => [match[1], match[2].replace(/^['"]|['"]$/g, "")]),
  );
}

function normalizeRepo(repo) {
  return repo
    .trim()
    .replace(/^https?:\/\/github\.com\//, "")
    .replace(/^github\.com\//, "")
    .replace(/\.git$/, "")
    .replace(/\/+$/, "");
}

function yamlQuote(value) {
  return `"${String(value).replaceAll("\\", "\\\\").replaceAll('"', '\\"')}"`;
}

function dateOnly(value) {
  return value.slice(0, 10);
}

const repos = new Set();

for (const filename of fs.readdirSync(postsDir)) {
  if (!filename.endsWith(".md")) {
    continue;
  }

  const frontMatter = parseFrontMatter(path.join(postsDir, filename));
  if (frontMatter.type === "project" && frontMatter["gh-repo"]) {
    repos.add(normalizeRepo(frontMatter["gh-repo"]));
  }
}

const sortedRepos = [...repos].sort();
const repoData = [];

for (const repo of sortedRepos) {
  const info = await requestJson(`/repos/${repo}`);
  const defaultBranch = info.default_branch || "master";
  const commits = await requestJson(`/repos/${repo}/commits?sha=${encodeURIComponent(defaultBranch)}&per_page=1`);
  const latestCommit = Array.isArray(commits) ? commits[0] : null;
  const latestCommitDate =
    latestCommit?.commit?.committer?.date || latestCommit?.commit?.author?.date || info.pushed_at;

  repoData.push({
    repo,
    created: dateOnly(info.created_at),
    defaultBranch,
    latestCommit: dateOnly(latestCommitDate),
  });
}

const lines = [
  "# Generated by scripts/update-github-repo-dates.mjs. Do not edit by hand.",
  "# Refresh with: npm run update:github-repos",
];

for (const repo of repoData) {
  lines.push(`${yamlQuote(repo.repo)}:`);
  lines.push(`  created: ${repo.created}`);
  lines.push(`  default_branch: ${yamlQuote(repo.defaultBranch)}`);
  lines.push(`  latest_commit: ${repo.latestCommit}`);
}

fs.writeFileSync(outFile, `${lines.join("\n")}\n`);
console.log(`Wrote ${repoData.length} repos to ${path.relative(process.cwd(), outFile)}`);
